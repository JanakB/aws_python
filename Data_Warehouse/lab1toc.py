string = 'Janak'
#getting first character of the string 
print(string[0])
#getting last character of the string 
print(string[-1])
#getting first 3 characters of a string 
print(string[:3])
#getting last 3 characters of a string 
print(string[-3:])
#getting a substring of length 3 from 4th character 
print(string[4:-1])
#Get a substring which contains all characters except the last 2 and the 1st character 
print(string[1:-2])
